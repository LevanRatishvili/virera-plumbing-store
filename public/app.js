  if (output.footer?.footerText?.includes("бғ“бғ”бғӣбғқ бғ•бғ”бғ бғЎбғҳбғҗ")) delete output.footer;
  if (Array.isArray(output.heroSlides) && output.heroSlides.some((slide) => String(slide.image || "").includes("plumb-") || String(slide.text || "").includes("бғ“бғ”бғӣбғқ бғӣбғҳбғӣбғҗбғ бғ—бғЈбғҡбғ”бғ‘бғ”бғ‘бғҳ"))) delete output.heroSlides;
  if (Array.isArray(output.services) && output.services.some((service) => String(service.description || "").includes("бғ§бғқбғ•бғ”бғҡбғ“бғҰбғҳбғЈбғ бғҳ бғҜбғҗбғңбғӣбғ бғ—бғ”бғҡбғқбғ‘бғҳбғЎ бғ–бғқбғ’бғҗбғ“бғҳ бғЁбғ”бғӨбғҗбғЎбғ”бғ‘бғҗ"))) delete output.services;
  if (Array.isArray(output.doctors) && output.doctors.some((doctor) => String(doctor.note || "").includes("бғ“бғ”бғӣбғқ бғһбғ бғқбғӨбғҳбғҡбғҳ") || String(doctor.image || "").includes("/assets/categories/"))) delete output.doctors;
