async function submitAppointment(event) {
  event.preventDefault();
  const form = event.currentTarget;
  const button = form.querySelector("button[type='submit']");
  const status = document.querySelector("#formStatus");
  const body = Object.fromEntries(new FormData(form));
  button.disabled = true;
  button.textContent = "бғҳбғ’бғ–бғҗбғ•бғңбғ”бғ‘бғҗ...";
  status.className = "form-status";
  status.textContent = "";
  try {
    const result = await api("/api/appointments", { method: "POST", body });
    status.classList.add("success-text");
    status.textContent = result.message || "бғ©бғҗбғ¬бғ”бғ бғҳбғЎ бғӣбғқбғ—бғ®бғқбғ•бғңбғҗ бғӣбғҳбғҰбғ”бғ‘бғЈбғҡбғҳбғҗ";
    form.reset();
  } catch (error) {
    status.classList.add("error-text");
    status.textContent = error.message;
  } finally {
    button.disabled = false;
    button.textContent = "бғӣбғқбғ—бғ®бғқбғ•бғңбғҳбғЎ бғ’бғҗбғ’бғ–бғҗбғ•бғңбғҗ";
  }
}

