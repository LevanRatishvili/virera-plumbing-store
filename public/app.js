  if (/Image path/i.test(message)) return "бғЎбғЈбғ бғҗбғ—бғҳбғЎ бғӣбғҳбғЎбғҗбғӣбғҗбғ бғ—бғҳ бғЈбғңбғ“бғҗ бғҳбғ§бғқбғЎ бғЈбғЎбғҗбғӨбғ бғ—бғ®бғқ бғҡбғқбғҷбғҗбғҡбғЈбғ бғҳ бғӨбғҗбғҳбғҡбғҳ.";
  if (/Too many items/i.test(message)) return "бғҗбғӣ бғЎбғ”бғҘбғӘбғҳбғҗбғЁбғҳ бғ«бғҗбғҡбғҳбғҗбғң бғ‘бғ”бғ•бғ бғҳ бғ©бғҗбғңбғҗбғ¬бғ”бғ бғҳбғҗ.";
  if (/Unsupported content section/i.test(message)) return "бғЎбғҗбғ бғ”бғ–бғ”бғ бғ•бғқ бғӨбғҗбғҳбғҡбғЁбғҳ бғЈбғӘбғңбғқбғ‘бғҳ бғЎбғ”бғҘбғӘбғҳбғҗбғҗ.";
